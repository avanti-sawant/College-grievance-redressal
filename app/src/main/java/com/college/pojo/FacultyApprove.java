package com.college.pojo;

public class FacultyApprove {
    String f_id;
    String f_name;
    String f_gender;
    String f_email;
    String f_phone;
    String f_password;
    String f_designation;
    String f_id_no;
    String f_department;
    String f_status;
    String f_time;

    public FacultyApprove(String f_id, String f_name, String f_gender, String f_email, String f_phone, String f_password, String f_designation, String f_id_no, String f_department, String f_status, String f_time) {
        this.f_id = f_id;
        this.f_name = f_name;
        this.f_gender = f_gender;
        this.f_email = f_email;
        this.f_phone = f_phone;
        this.f_password = f_password;
        this.f_designation = f_designation;
        this.f_id_no = f_id_no;
        this.f_department = f_department;
        this.f_status = f_status;
        this.f_time = f_time;
    }

    public String getF_id() {
        return f_id;
    }

    public void setF_id(String f_id) {
        this.f_id = f_id;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public String getF_gender() {
        return f_gender;
    }

    public void setF_gender(String f_gender) {
        this.f_gender = f_gender;
    }

    public String getF_email() {
        return f_email;
    }

    public void setF_email(String f_email) {
        this.f_email = f_email;
    }

    public String getF_phone() {
        return f_phone;
    }

    public void setF_phone(String f_phone) {
        this.f_phone = f_phone;
    }

    public String getF_password() {
        return f_password;
    }

    public void setF_password(String f_password) {
        this.f_password = f_password;
    }

    public String getF_designation() {
        return f_designation;
    }

    public void setF_designation(String f_designation) {
        this.f_designation = f_designation;
    }

    public String getF_id_no() {
        return f_id_no;
    }

    public void setF_id_no(String f_id_no) {
        this.f_id_no = f_id_no;
    }

    public String getF_department() {
        return f_department;
    }

    public void setF_department(String f_department) {
        this.f_department = f_department;
    }

    public String getF_status() {
        return f_status;
    }

    public void setF_status(String f_status) {
        this.f_status = f_status;
    }

    public String getF_time() {
        return f_time;
    }

    public void setF_time(String f_time) {
        this.f_time = f_time;
    }




}
