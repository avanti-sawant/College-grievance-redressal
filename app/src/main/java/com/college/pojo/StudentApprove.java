package com.college.pojo;

public class StudentApprove {
    String s_id;
    String s_name;
    String cd_id;
    String b_id;
    String s_admission_no;
    String s_email;
    String s_phone;
    String s_password;
    String s_gender;
    String s_status;
    String s_time;

    public StudentApprove(String s_id, String s_name, String cd_id, String b_id, String s_admission_no, String s_email, String s_phone, String s_password, String s_gender, String s_status, String s_time) {
        this.s_id = s_id;
        this.s_name = s_name;
        this.cd_id = cd_id;
        this.b_id = b_id;
        this.s_admission_no = s_admission_no;
        this.s_email = s_email;
        this.s_phone = s_phone;
        this.s_password = s_password;
        this.s_gender = s_gender;
        this.s_status = s_status;
        this.s_time = s_time;
    }

    public String getS_id() {
        return s_id;
    }

    public void setS_id(String s_id) {
        this.s_id = s_id;
    }

    public String getS_name() {
        return s_name;
    }

    public void setS_name(String s_name) {
        this.s_name = s_name;
    }

    public String getCd_id() {
        return cd_id;
    }

    public void setCd_id(String cd_id) {
        this.cd_id = cd_id;
    }

    public String getB_id() {
        return b_id;
    }

    public void setB_id(String b_id) {
        this.b_id = b_id;
    }

    public String getS_admission_no() {
        return s_admission_no;
    }

    public void setS_admission_no(String s_admission_no) {
        this.s_admission_no = s_admission_no;
    }

    public String getS_email() {
        return s_email;
    }

    public void setS_email(String s_email) {
        this.s_email = s_email;
    }

    public String getS_phone() {
        return s_phone;
    }

    public void setS_phone(String s_phone) {
        this.s_phone = s_phone;
    }

    public String getS_password() {
        return s_password;
    }

    public void setS_password(String s_password) {
        this.s_password = s_password;
    }

    public String getS_gender() {
        return s_gender;
    }

    public void setS_gender(String s_gender) {
        this.s_gender = s_gender;
    }

    public String getS_status() {
        return s_status;
    }

    public void setS_status(String s_status) {
        this.s_status = s_status;
    }

    public String getS_time() {
        return s_time;
    }

    public void setS_time(String s_time) {
        this.s_time = s_time;
    }




}
